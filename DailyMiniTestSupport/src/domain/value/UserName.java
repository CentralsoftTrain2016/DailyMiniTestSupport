package domain.value;

public class UserName extends Value{

	private String userName;	// ユーザ名

	/* コンストラクタ */
	public UserName(String userName){
		super();
		this.userName = userName;
	}

	/* getメソッド */
	public String getUserName(){
		return userName;
	}

	/* toStringメソッド */
	@Override
	public String toString(){
		return userName;
	}

}

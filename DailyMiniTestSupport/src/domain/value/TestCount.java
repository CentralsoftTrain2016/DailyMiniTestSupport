package domain.value;

public class TestCount extends Value{

	private int testCount;	// 文字列

	/* コンストラクタ */
	public TestCount(int testCount){
		super();
		this.testCount = testCount;
	}

	/* getメソッド */
	public int getTestCount(){
		return testCount;
	}

	/* toStringメソッド */
	@Override
	public String toString(){
		return String.valueOf(testCount);
	}

}

package domain.value;

public class Score extends Value{

	private float score;	// 点数

	/* コンストラクタ */
	public Score(float score){
		super();
		this.score = score;
	}

	/* getメソッド */
	public float getValue(){
		return score;
	}

	/* toStringメソッド */
	@Override
	public String toString(){
		return String.valueOf(score);
	}

}
